package com.renault.rnet.idp.bean;

/**
 * Parameters fetched in the url
 * @author rng
 *
 */
public class Parameter {

	private String uid;
	private String sp;
	
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getSp() {
		return sp;
	}
	public void setSp(String spid) {
		this.sp = spid;
	}
	
	
}
